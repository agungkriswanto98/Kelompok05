class bangun {
public float Lpersegi(float a,float b){
return (a*b);
}
public float Kpersegi(float a, float b){
return (2*(a+b));
}

public float Lsegitiga(float a,float b){
    return (a*b/2);
}
public float Ksegitiga(float a,float b){
    return (a+(2*b));
}

public float Llingkaran(float a){
    return  (22/7)*a*a;
}
public float Klingkaran(float a){
    return (22/7)*2*a;
}
public float Ljajar(float a,float t){
    return (a*t);
}
public float Kjajar(float a,float siMir){
    return (2*(a+siMir));
}
public float Lpersegipanjang(float a,float b){
return (a*b);
}
public float Kpersegipanjang(float a, float b){
return (2*(a+b));
}

public float volBalok(float panjang, float lebar, float tinggi) {
     return (panjang*lebar*tinggi);
}

public float lpBalok(float panjang, float lebar,float tinggi) {
     return (2*((panjang*lebar)+(panjang*tinggi)+(lebar*tinggi)));
    }

public float volPrismaSegitiga(float alas, float tinggi,float sisi) {
       return (1/2*alas*tinggi);
    }

public float lpPrismaSegitiga(float alas, float sisi,float sisiMir, float tinggi) {
        return ((2*1/2*sisi*sisiMir)+((sisi+sisiMir+alas)*tinggi));
    }
public float volTabung(float jari, float tinggi) {
       return (22/7*jari*jari*tinggi);
    }

public float lpTabung(float jari,float tinggi) {
        return (2*22/7*jari*(jari+tinggi));
    }

}

