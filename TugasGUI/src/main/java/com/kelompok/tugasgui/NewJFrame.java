    private void jButtonHitung3DActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonHitung3DActionPerformed
        // TODO add your handling code here:
        float panjang,lebar,alas,tinggi,jari,sisiMir,sisi,diagonal,lp,vol;
        panjang = Float.valueOf(jTextPanjang.getText());
        lebar = Float.valueOf(jTextLebar.getText());
        alas = Float.valueOf(jTextAlas.getText());
        tinggi = Float.valueOf(jTextTinggi.getText());
        jari = Float.valueOf(jTextJariJari.getText());
        sisiMir = Float.valueOf(jTextSisiMiring.getText());
        sisi = Float.valueOf(jTextSisi.getText());
        diagonal = Float.valueOf(jTextDiagonal.getText());
        
       
        bangun Bangun = new bangun ();
        if (jRadioPrismaSegitiga.isSelected()==true) {
            jTextHasilVolume.setText(String.valueOf(Bangun.volPrismaSegitiga(alas,sisi, tinggi)));
            jTextHasilLuasPermukaan.setText(String.valueOf(Bangun.lpPrismaSegitiga(alas,sisi,sisiMir, tinggi)));
        }
        else if  (jRadioBalok.isSelected()==true) {
            jTextHasilVolume.setText(String.valueOf(Bangun.volBalok(panjang, lebar, tinggi)));
            jTextHasilLuasPermukaan.setText(String.valueOf(Bangun.lpBalok(panjang, lebar,tinggi)));
        }
        else if  (jRadioTabung.isSelected()==true) {
            jTextHasilVolume.setText(String.valueOf(Bangun.volTabung(jari,tinggi)));
            jTextHasilLuasPermukaan.setText(String.valueOf(Bangun.lpTabung(jari,tinggi)));
        }
    }//GEN-LAST:event_jButtonHitung3DActionPerformed

